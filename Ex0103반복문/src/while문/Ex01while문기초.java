package while문;

public class Ex01while문기초 {

	public static void main(String[] args) {

		//while구조
		//hellow world 5번 출력하는 코드
		
		//반복횟수를 정해주는 변수선언
		int num = 0;                                   //0부터 시작
		while(num<5) {                                 //1씩 증가하면서 5이하까지 계속 반복
		System.out.println("hellow world");
		num++;                                         //증감연산자
		}
		
	}

}
