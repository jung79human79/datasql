package dowhile문;

public class Ex01dowhile문기초 {

	public static void main(String[] args) {

		//do-while문 구조
		do {
			System.out.println("반드시 한 번 실행시키는 문장");
		} while(false);                                      
		//false가능 //무조건 do을 실행시키므로~
		//do-while문은 반드시 한번은 실행시켜야 할 코드가 있을때 사용
		//-->검사조건이 거짓이어도 반드시 한 번은 실행된다.
	}

}
